#!/usr/bin/env bash
# ================================================================
# Скрипт для проверки корректности оформления Pull Request (PR)
# ================================================================
# Ожидаемый формат заголовка:
#   <module> | <type> | #<issue_number> - <описание>
#
# Пример:
#   auth | feature | #42 - Добавить проверку токена
#
# В конце описания PR ОБЯЗАТЕЛЬНО:
#   Changelog: <type>
# ================================================================

set -e

# --- Цвета ---
RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
NC='\033[0m'

FAILURE=false

# ------------------------------------------------
# Проверка переменных окружения
# ------------------------------------------------
if [ -z "$PR_TITLE" ]; then
  echo -e "${RED}❌ Не задана переменная окружения PR_TITLE.${NC}"
  exit 1
fi

if [ -z "$PR_BODY" ]; then
  echo -e "${RED}❌ Не задана переменная окружения PR_BODY.${NC}"
  exit 1
fi

# ------------------------------------------------
# Разрешённые значения
# ------------------------------------------------
MODULES="generic|auth"
TYPES="feature|fix|docs|refactoring"

# ------------------------------------------------
# Регулярные выражения
# ------------------------------------------------
TITLE_MASK="^(${MODULES})[[:space:]]\|[[:space:]](${TYPES})[[:space:]]\|[[:space:]]\#[[:digit:]]+[[:space:]]-[[:space:]].+"
CHANGELOG_MASK="^Changelog:[[:space:]](${TYPES})$"

# ------------------------------------------------
# Берём последнюю непустую строку PR_BODY
# ------------------------------------------------
PR_BODY_LAST_LINE="$(printf '%s\n' "$PR_BODY" | sed '/^[[:space:]]*$/d' | tail -n 1)"

# ================================================================
# 1. Проверка заголовка PR
# ================================================================
echo
echo "##### 1. Проверка заголовка PR"
echo "Текущий заголовок:"
echo "  $PR_TITLE"

if [[ "$PR_TITLE" =~ $TITLE_MASK ]]; then
  echo -e "${GREEN}✅ Заголовок PR корректен.${NC}"
else
  echo -e "${RED}❌ Некорректный заголовок PR.${NC}"
  echo -e "${BLUE}Ожидаемый формат:${NC}"
  echo -e "  <module> | <type> | #<issue> - <описание>"
  echo -e "${BLUE}Пример:${NC}"
  echo -e "  auth | feature | #42 - Добавить проверку токена"
  echo -e "${BLUE}Допустимые модули:${NC} ${MODULES}"
  echo -e "${BLUE}Допустимые типы:${NC} ${TYPES}"
  FAILURE=true
fi

# ================================================================
# 2. Проверка Changelog
# ================================================================
echo
echo "##### 2. Проверка Changelog в конце описания PR"

if [[ "$PR_BODY_LAST_LINE" =~ $CHANGELOG_MASK ]]; then
  echo -e "${GREEN}✅ Changelog оформлен корректно.${NC}"
else
  echo -e "${RED}❌ Не найден корректный Changelog в конце описания PR.${NC}"
  echo -e "${BLUE}Описание PR должно заканчиваться строкой:${NC}"
  echo -e "  Changelog: <type>"
  echo -e "${BLUE}Допустимые типы:${NC} ${TYPES}"
  FAILURE=true
fi

# ================================================================
# Итог
# ================================================================
echo
if [ "$FAILURE" = true ]; then
  echo -e "${RED}❌ Проверка PR не пройдена.${NC}"
  exit 1
else
  echo -e "${GREEN}🎉 Проверка PR успешно пройдена.${NC}"
  exit 0
fi
